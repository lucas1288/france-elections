// Build the département-level cross-election history file (two-axis P5).
//
// Aggregates every manifest election's dept-level round{N}.json into ONE small
// file, with scores rolled up by POLITICAL FAMILY (families.json + each
// palette's `family` memberships — the P3 registry's purpose): per département
// (+ 'FR' = national, summed from dept entries, which sum to national exactly),
// per election × round, participation % and % of expressed votes per family.
//
// Output: public/data/elections/history/depts.json (~a few hundred KB).
// Re-run after ingesting an election or editing a palette/families.json
// (validate-families.mjs is the gate; this script also errors on unmapped
// party codes). Consumed by useDeptHistory() → the "Historique" section of the
// dept insight panel.
//
// Usage: node scripts/build-dept-history.mjs

import fs from 'node:fs'
import path from 'node:path'

const ROOT = path.resolve(import.meta.dirname, '..')
const ELEC = path.join(ROOT, 'public/data/elections')

const readJson = (p) => JSON.parse(fs.readFileSync(p, 'utf8'))

const manifest = readJson(path.join(ELEC, 'index.json'))
const registry = readJson(path.join(ELEC, 'families.json'))

const round1dp = (n) => Math.round(n * 10) / 10

/** dept entry → { part, fam } via the election's palette family memberships. */
function pointOf(entry, famOfParty, ctx) {
  const famVotes = new Map()
  for (const c of entry.candidates) {
    const fam = famOfParty.get(c.party)
    if (!fam) throw new Error(`${ctx}: unmapped party code '${c.party}' (run validate-families.mjs)`)
    famVotes.set(fam, (famVotes.get(fam) ?? 0) + c.votes)
  }
  const expressed = entry.expressedVotes
  const fam = {}
  if (expressed > 0) {
    // Spectrum order keeps the JSON stable/diffable.
    for (const [id] of Object.entries(registry.families)) {
      const v = famVotes.get(id)
      if (v) fam[id] = round1dp((v / expressed) * 100)
    }
  }
  return {
    part: entry.registeredVoters > 0 ? round1dp((entry.turnout / entry.registeredVoters) * 100) : 0,
    fam,
  }
}

const depts = {} // code → { name, series: [] }
const pushPoint = (code, name, t, y, r, point) => {
  const d = (depts[code] ??= { name, series: [] })
  d.series.push({ t, y, r, ...point })
}

for (const e of manifest.elections) {
  const dir = path.join(ELEC, e.type, String(e.year))
  const palette = readJson(path.join(dir, 'palette.json'))
  const famOfParty = new Map(
    Object.entries(palette.parties ?? {}).map(([code, def]) => [code, def.family]),
  )
  for (const fam of famOfParty.values()) {
    if (fam && !registry.families[fam]) throw new Error(`${e.type} ${e.year}: unknown family '${fam}'`)
  }

  for (let r = 1; r <= e.rounds; r++) {
    const data = readJson(path.join(dir, `round${r}.json`))
    const ctx = `${e.type} ${e.year} R${r}`

    // National aggregate from the dept entries (they sum to national exactly).
    const nat = { registeredVoters: 0, turnout: 0, expressedVotes: 0, candidates: new Map() }
    for (const entry of data.communes) {
      pushPoint(entry.inseeCode, entry.name, e.type, e.year, r, pointOf(entry, famOfParty, ctx))
      nat.registeredVoters += entry.registeredVoters
      nat.turnout += entry.turnout
      nat.expressedVotes += entry.expressedVotes
      for (const c of entry.candidates) {
        nat.candidates.set(c.party, (nat.candidates.get(c.party) ?? 0) + c.votes)
      }
    }
    const natEntry = {
      registeredVoters: nat.registeredVoters,
      turnout: nat.turnout,
      expressedVotes: nat.expressedVotes,
      candidates: [...nat.candidates.entries()].map(([party, votes]) => ({ party, votes })),
    }
    pushPoint('FR', 'France entière', e.type, e.year, r, pointOf(natEntry, famOfParty, ctx))
  }
}

// Stable ordering: per dept, chronological then by round.
const TYPE_MONTH = { presidential: 4, legislative: 6 }
for (const d of Object.values(depts)) {
  d.series.sort((a, b) => a.y - b.y || (TYPE_MONTH[a.t] ?? 0) - (TYPE_MONTH[b.t] ?? 0) || a.r - b.r)
}

const out = {
  comment:
    'GENERATED by scripts/build-dept-history.mjs — do not edit. Département-level cross-election series by political family (families.json ids), % of expressed votes; part = participation %. FR = national.',
  depts,
}
const outDir = path.join(ELEC, 'history')
fs.mkdirSync(outDir, { recursive: true })
const outPath = path.join(outDir, 'depts.json')
fs.writeFileSync(outPath, JSON.stringify(out))

// ── Sanity report ───────────────────────────────────────────────────────────
const kb = (fs.statSync(outPath).size / 1024).toFixed(0)
const nDepts = Object.keys(depts).length
const fr = depts.FR.series
console.log(`history/depts.json written: ${nDepts} territories, ${fr.length} points each (FR), ${kb} KB`)
const show = (t, y, r) => {
  const p = fr.find((s) => s.t === t && s.y === y && s.r === r)
  if (!p) return
  const top = Object.entries(p.fam).sort((a, b) => b[1] - a[1]).slice(0, 5)
    .map(([f, v]) => `${f} ${v}`).join(' · ')
  console.log(`  FR ${t} ${y} R${r}: part ${p.part} — ${top}`)
}
for (const e of manifest.elections) for (let r = 1; r <= e.rounds; r++) show(e.type, e.year, r)
const herault = depts['34']?.series.find((s) => s.t === 'presidential' && s.y === 2017 && s.r === 1)
if (herault) {
  console.log('  Hérault prés 2017 R1:', JSON.stringify(herault.fam))
}
