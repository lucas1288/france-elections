#!/usr/bin/env node
/**
 * Generates src/utils/territoryBBoxes.ts — lng/lat bounding boxes for every
 * département and circonscription, decoded from the shipped PMTiles, so the
 * territory navigator can fitBounds any territory without geometry lookups.
 *
 * Keys are the app's data codes: INSEE dept codes ('34', '2A', '971'…) and
 * INSEE-derived circo codes ('3401', '97101'…) — overseas circo Z-codes from
 * the tiles are translated back to INSEE here.
 *
 * Usage: node scripts/build-territory-bboxes.mjs
 * Requires: tippecanoe-decode on PATH. Re-run after any tile rebuild.
 */
import { execFileSync } from 'node:child_process'
import { writeFileSync } from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const root = path.join(path.dirname(fileURLToPath(import.meta.url)), '..')
const ADMIN = path.join(root, 'public/data/tiles/france-admin.pmtiles')
const CIRCO = path.join(root, 'public/data/tiles/circonscriptions.pmtiles')
const OUT = path.join(root, 'src/utils/territoryBBoxes.ts')

// Same mapping as FranceMap.tsx INSEE_TO_CIRCO_ZCODE, inverted.
const ZPREFIX_TO_INSEE = { ZA: '971', ZB: '972', ZC: '973', ZD: '974', ZS: '975', ZM: '976' }

// Decode one layer at a single zoom (dept layer keeps all features at every
// zoom; the circo tileset is small). z5 keeps output modest while every
// feature is present; bboxes only need coarse geometry.
function decode(file, layer, zoom) {
  const out = execFileSync('tippecanoe-decode', ['-l', layer, `-Z${zoom}`, `-z${zoom}`, file], {
    maxBuffer: 1024 * 1024 * 512,
    encoding: 'utf8',
  })
  return JSON.parse(out)
}

function walkCoords(coords, bbox) {
  if (typeof coords[0] === 'number') {
    const [lng, lat] = coords
    if (lng < bbox[0]) bbox[0] = lng
    if (lat < bbox[1]) bbox[1] = lat
    if (lng > bbox[2]) bbox[2] = lng
    if (lat > bbox[3]) bbox[3] = lat
  } else {
    for (const c of coords) walkCoords(c, bbox)
  }
}

function collectBBoxes(decoded, codeProp, translateCode = (c) => c) {
  const boxes = new Map()
  const walk = (node) => {
    if (!node || typeof node !== 'object') return
    if (node.type === 'Feature' && node.properties && node.geometry) {
      const raw = node.properties[codeProp]
      if (raw != null) {
        const code = translateCode(String(raw))
        const bbox = boxes.get(code) ?? [Infinity, Infinity, -Infinity, -Infinity]
        walkCoords(node.geometry.coordinates, bbox)
        boxes.set(code, bbox)
      }
      return
    }
    if (Array.isArray(node.features)) node.features.forEach(walk)
    else for (const v of Object.values(node)) if (v && typeof v === 'object') walk(v)
  }
  walk(decoded)
  return boxes
}

const round = (n) => Math.round(n * 1000) / 1000

console.log('Decoding départements…')
const deptBoxes = collectBBoxes(decode(ADMIN, 'departements', 5), 'code')
console.log(`  ${deptBoxes.size} départements`)

console.log('Decoding circonscriptions…')
// z8: the circo tileset drops some small (dense urban) circos at low zooms.
const circoBoxes = collectBBoxes(decode(CIRCO, 'circonscriptions', 8), 'codeCirconscription', (c) => {
  const insee = ZPREFIX_TO_INSEE[c.slice(0, 2)]
  return insee ? insee + c.slice(2) : c
})
console.log(`  ${circoBoxes.size} circonscriptions`)

const fmt = (boxes) =>
  [...boxes.entries()]
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([code, b]) => `  '${code}': [${b.map(round).join(', ')}],`)
    .join('\n')

writeFileSync(
  OUT,
  `// GENERATED by scripts/build-territory-bboxes.mjs — do not edit by hand.
// Lng/lat bounding boxes [west, south, east, north] decoded from the shipped
// PMTiles. Keys are data codes (INSEE); overseas circo Z-codes translated.
// Re-run the script after any tile rebuild.

export type BBox = [number, number, number, number]

export const DEPT_BBOXES: Record<string, BBox> = {
${fmt(deptBoxes)}
}

export const CIRCO_BBOXES: Record<string, BBox> = {
${fmt(circoBoxes)}
}
`,
)
console.log(`Wrote ${OUT}`)
