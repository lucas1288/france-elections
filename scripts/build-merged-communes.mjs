/**
 * Generates src/utils/mergedCommunes.ts — a mapping from obsolete commune
 * codes present in the PMTiles geometry to their current INSEE code.
 *
 * Why: the commune geometry in france-admin.pmtiles predates several waves of
 * "communes nouvelles" mergers, so ~410 polygons carry codes that no longer
 * exist in the 2022 election data (the merged commune keeps one constituent's
 * code). Those polygons render blank and have no hover/click results.
 *
 * Method: decode the commune layer at z9 with tippecanoe-decode, diff feature
 * codes against the election data, then reverse-geocode each orphan polygon's
 * centroid via geo.api.gouv.fr (lat/lon → current commune). Results are
 * validated against the election data before being written.
 *
 * Requires: tippecanoe-decode on PATH. Run: node scripts/build-merged-communes.mjs
 */

import { execSync } from 'child_process'
import { readFileSync, writeFileSync } from 'fs'

const TILES = 'public/data/tiles/france-admin.pmtiles'
const DATA = 'public/data/elections/presidential/2022/round1-communes-choropleth.json'
const OUT = 'src/utils/mergedCommunes.ts'

console.log('Decoding commune layer at z9 (this takes a moment)…')
const decoded = JSON.parse(
  execSync(`tippecanoe-decode -z 9 -l communes ${TILES}`, {
    maxBuffer: 4 * 1024 * 1024 * 1024,
  }).toString(),
)

// Collect one representative ring per feature code
const rings = new Map()
function walk(o) {
  if (o && o.type === 'Feature' && o.properties?.code) {
    if (!rings.has(o.properties.code)) {
      let c = o.geometry.coordinates
      while (Array.isArray(c[0][0])) c = c[0]
      rings.set(o.properties.code, c)
    }
  } else if (o && typeof o === 'object') {
    for (const k in o) walk(o[k])
  }
}
walk(decoded)
console.log(`Found ${rings.size} commune codes in tiles`)

const dataCodes = new Set(
  JSON.parse(readFileSync(DATA, 'utf8')).communes.map((c) => c.inseeCode),
)
const orphans = [...rings.keys()].filter((c) => !dataCodes.has(c)).sort()
console.log(`${orphans.length} orphan codes (in tiles, no election data)`)

function centroid(ring) {
  let lng = 0, lat = 0
  for (const [x, y] of ring) { lng += x; lat += y }
  return [lng / ring.length, lat / ring.length]
}

const mapping = {}
const failures = []
for (let i = 0; i < orphans.length; i++) {
  const code = orphans[i]
  const [lon, lat] = centroid(rings.get(code))
  try {
    const res = await fetch(
      `https://geo.api.gouv.fr/communes?lat=${lat}&lon=${lon}&fields=code`,
    )
    const hits = res.ok ? await res.json() : []
    const current = hits[0]?.code
    if (current && current !== code && dataCodes.has(current)) {
      mapping[code] = current
    } else {
      failures.push({ code, current: current ?? null })
    }
  } catch {
    failures.push({ code, current: 'fetch-error' })
  }
  if ((i + 1) % 50 === 0) console.log(`  ${i + 1}/${orphans.length}…`)
  await new Promise((r) => setTimeout(r, 80)) // stay polite with the API
}

console.log(`Mapped ${Object.keys(mapping).length}, failed ${failures.length}`)
if (failures.length) console.log('Failures:', JSON.stringify(failures))

const entries = Object.entries(mapping)
  .sort(([a], [b]) => a.localeCompare(b))
  .map(([o, n]) => `  '${o}': '${n}',`)
  .join('\n')

writeFileSync(
  OUT,
  `/**
 * Obsolete commune code (present in france-admin.pmtiles geometry) → current
 * INSEE code (present in election data). These are communes absorbed into
 * "communes nouvelles" after the tile geometry was produced but before the
 * 2022 election. Generated by scripts/build-merged-communes.mjs — do not edit
 * by hand; re-run the script if the tiles or election data change.
 */
export const MERGED_COMMUNE_TO_CURRENT: Record<string, string> = {
${entries}
}
`,
)
console.log(`Wrote ${OUT}`)
